/**
 * Recursively replace string-literal types with the generic `string`
 * type. Without this, `as const` on the source catalog would make
 * every key's value a literal — and `en-US.ts` translations would fail
 * the type check because `'Cancel'` isn't assignable to `'取消'`.
 */
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends Record<string, unknown>
      ? DeepStringify<T[K]>
      : T[K]
}

/**
 * Derived type — every locale catalog must mirror this shape but is
 * free to provide any concrete strings. Keeps translations from
 * drifting silently while allowing real translations.
 */
export type MessageSchema = DeepStringify<typeof messages>
